function toCssVars(t) {
  return {
    '--background': t.background,
    '--foreground': t.text,
    '--card': t.surface,
    '--card-foreground': t.text,
    '--popover': t.surfaceElevated,
    '--popover-foreground': t.text,
    '--primary': t.gold,
    '--primary-foreground': t.backgroundDeep,
    '--secondary': t.surfaceElevated,
    '--secondary-foreground': t.text,
    '--muted': t.surface,
    '--muted-foreground': t.textMuted,
    '--accent': t.surfaceElevated,
    '--accent-foreground': t.text,
    '--destructive': t.danger,
    '--border': t.border,
    '--input': t.borderLight,
    '--ring': t.gold,
    '--chart-1': t.gold,
    '--chart-2': t.cyan,
    '--chart-3': t.success,
    '--chart-4': t.warning,
    '--chart-5': t.pink,
    '--sidebar': t.surface,
    '--sidebar-foreground': t.text,
    '--sidebar-primary': t.gold,
    '--sidebar-primary-foreground': t.backgroundDeep,
    '--sidebar-accent': t.surfaceElevated,
    '--sidebar-accent-foreground': t.text,
    '--sidebar-border': t.border,
    '--sidebar-ring': t.gold,
  };
}

